// --- Utilidad para generar folios ---

let ultimoFolioNumero = 78;

export function generarFolioOT(): string {
  const año = new Date().getFullYear();
  ultimoFolioNumero += 1;
  return `OT-${año}-${String(ultimoFolioNumero).padStart(3, '0')}`;
}

export function generarFolioChecklist(): string {
  const año = new Date().getFullYear();
  return `CHK-${año}-${String(Date.now()).slice(-6)}`;
}
